    public static void genGraph() {
        int rows = map.length;
        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < map[i].length; j++) {
                if (map[i][j] == 1) {
                    if (j + 1 < map[i].length && map[i][j + 1] == 1) {
                        GraphOfSystem.addUnDirectedEdge(new Location(i, j),
                                new Location(i, j + 1));
                    }
                    // Add edge downwards
                    if (i + 1 < rows && map[i + 1][j] == 1) {
                        GraphOfSystem.addUnDirectedEdge(new Location(i, j),
                                new Location(i + 1, j));
                    }
                }
            }
        }
        findNearestOne();
    }
